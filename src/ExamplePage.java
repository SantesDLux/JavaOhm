    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        // TODO add your handling code here:
        optionCmbBox = cmbCalculo.getSelectedItem().toString();
        double firstVariable = Double.parseDouble(txtFirstVariable.getText());
        double secondVariable = Double.parseDouble(txtSecondVariable.getText());
        Double result;
        switch (optionCmbBox) {
            case "Intensidad" -> result = calcularIntensidad(firstVariable, secondVariable);
            case "Resistencia" -> result = calcularResistencia(firstVariable, secondVariable);
            case "Voltaje" -> result = calcularVoltaje(firstVariable, secondVariable);
            default -> throw new AssertionError();
        }
        txtResult.setText(result.toString());
    }//GEN-LAST:event_btnCalcularActionPerformed

    private void cmbCalculoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbCalculoActionPerformed
        changeControlTexts();
    }//GEN-LAST:event_cmbCalculoActionPerformed

    private void changeControlTexts() throws AssertionError {
        // TODO add your handling code here:
        optionCmbBox = cmbCalculo.getSelectedItem().toString();
        switch (optionCmbBox) {
            case "Intensidad" -> {
                lblFirstVariable.setText("Voltaje");
                lblSecondVariable.setText("Resistencia");
            }
            case "Resistencia" -> {
                lblFirstVariable.setText("Voltaje");
                lblSecondVariable.setText("Intensidad");
            }
            case "Voltaje" -> {
                lblFirstVariable.setText("Intensidad");
                lblSecondVariable.setText("Resistencia");
            }
            default -> throw new AssertionError();
        }
    }

    private double calcularResistencia(double voltaje, double intensidad) {
        return voltaje / intensidad;
    }

    private double calcularVoltaje(double intensidad, double resistencia) {
        return intensidad*resistencia;
    }

    private double calcularIntensidad(double voltaje, double resitencia) {
        return voltaje/resitencia;
    }

